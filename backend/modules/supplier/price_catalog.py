def lookup_price(product_name: str, budget: float = 0, quantity: float = 1) -> Optional[dict]:
    """
    Match product name against catalog and return price estimate.
    Returns None if no match found.

    Dict keys:
      unit_price_kzt  float — wholesale price per unit
      unit            str   — unit of measurement
      country         str   — best source country (KZ/RU/CN)
      confidence      int   — 0-100 match confidence
      match_keyword   str   — what triggered the match
    """
    name_lower = product_name.lower().strip()
    # Also try with words sorted — handles reversed Russian word order
    # e.g. "средство моющее" vs "моющее средство"
    name_words_sorted = " ".join(sorted(name_lower.split()))

    best_match = None
    best_kw_len = 0  # prefer longer/more specific keyword matches

    for entry in PRICE_CATALOG:
        for kw in entry["kw"]:
            # Direct substring match (highest priority)
            if kw in name_lower:
                if len(kw) > best_kw_len:
                    best_match = entry
                    best_kw_len = len(kw)
            # Word-sorted match (handles reversed adjective-noun order in Russian)
            elif len(kw) > 5:
                kw_sorted = " ".join(sorted(kw.split()))
                if kw_sorted in name_words_sorted and len(kw) > best_kw_len:
                    best_match = entry
                    best_kw_len = len(kw)

    if not best_match:
        return None

    unit_price = best_match["price"]

    # Sanity cross-check: if budget and quantity are known,
    # verify unit_price × qty is within 10×–0.05× of budget.
    adjusted_conf = best_match["conf"]
    if budget > 0 and quantity > 0:
        expected_total = unit_price * quantity
        ratio = expected_total / budget
        if ratio > 10 or ratio < 0.05:
            adjusted_conf = max(28, adjusted_conf - 30)
        elif ratio > 5 or ratio < 0.1:
            adjusted_conf = max(35, adjusted_conf - 15)

    return {
        "unit_price_kzt": float(unit_price),
        "unit":           best_match["unit"],
        "country":        best_match["country"],
        "confidence":     adjusted_conf,
        "match_keyword":  max(best_match["kw"], key=len),
    }
